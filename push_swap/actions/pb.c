#include "../push_swap.h"

void	pb(t_stack *a, t_stack *b)
{
	int	i;

	i = a->size;
	if (a->size > 0)
	{
		b->stack[0] = a->stack[0];
		while (i > 0)
		{
			a->stack[i] = a->stack[i - 1];
			i--;
		}
		a->size--;
		b->size++;
	}
	printf("pb\n");
}
