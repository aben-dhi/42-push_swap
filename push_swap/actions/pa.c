#include "../push_swap.h"

void	pa(t_stack *a, t_stack *b)
{
	int	i;

	i = b->size;
	if (b->size > 0)
	{
		while (i > 0)
		{
			b->stack[i] = b->stack[i - 1];
			i--;
		}
		b->size--;
		a->size++;
		a->stack[0] = b->stack[0];
	}
	write(1, "pa\n", 4);
}
