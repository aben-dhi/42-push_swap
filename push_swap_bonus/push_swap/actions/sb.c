#include "../push_swap.h"

void	sb(t_stack *b, int print)
{
	int	tmp;

	tmp = 0;
	if (b->size > 1)
	{
		tmp = b->stack[0];
		b->stack[0] = b->stack[1];
		b->stack[1] = tmp;
		if (print == 1)
			write(1, "sb\n", 3);
	}
}
